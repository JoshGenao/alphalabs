"""Declarative CLI command surface for ATP operator workflows.

This module is the single source of truth for the ATP command-line
interface described by ``API-4`` in ``feature_list.json`` and traced to
``SRS-API-001`` and ``SRS-SAFE-001`` in ``docs/SRS.md`` §7. It declares
command groups, per-command argument shapes, exit-code semantics, and
the local-shell access policy. It does **not** wire up real handlers:
concrete behaviours land with the downstream features that own each
operator workflow (``EXE-1``, ``ORCH-1``, ``RESV-1``, ``LOG-1``,
``NOTIF-1``).

The contract surface is introspectable at runtime via
:data:`COMMANDS`, and is rendered to a deterministic JSON manual by
:mod:`atp_cli.manual`. The frozen snapshot
``python/atp_cli/manual.json`` is regenerated by
``python tools/cli_check.py --update``.

SRS trace
---------
``SRS-API-001`` (REST/CLI/dashboard operator workflows) and
``SRS-SAFE-001`` (kill switch via dashboard, CLI, REST API), with
group-level traces to ``SRS-ORCH-004``/``SRS-ORCH-005`` (lifecycle,
version, rollback), ``SYS-2c``/``SYS-2d`` (live designation),
``SRS-RESV-003``..``SRS-RESV-006`` (Hot-Swap), ``SYS-76`` (readiness),
``SRS-LOG-001`` (logs), ``SRS-NOTIF-001`` (alerts), and ``SRS-SEC-002``
(loopback / single-user / local-shell policy).
"""

from __future__ import annotations

from dataclasses import dataclass, field
from enum import IntEnum, StrEnum

# --------------------------------------------------------------------------- #
# Access policy  [SRS-SEC-002, StRS C-6]
# --------------------------------------------------------------------------- #


ACCESS_MODEL: str = "local-shell"
"""CLI access model.

Per ``SRS-SEC-002`` and the API-4 ``Local shell access`` constraint,
the operator CLI runs only under the local operator account on the ATP
host. There is no remote-CLI mode, no auth tokens, and no RBAC.
"""

AUTH_MODEL: str = "local-single-user"
"""Auth model identifier shared with the REST/WebSocket surfaces.

The CLI trusts the local caller (single operator, ``StRS`` C-6) and
authenticates by virtue of OS-level shell access only.
"""

CLI_ENTRY_POINT: str = "atp"
"""Console script name exposed to operators (``atp <group> <command>``)."""

CLI_PROGRAM: str = "python -m atp_cli"
"""Module-invocation form used in tests and examples."""


# --------------------------------------------------------------------------- #
# Groups, exit codes, and command shape
# --------------------------------------------------------------------------- #


class Group(StrEnum):
    """Top-level command groups covered by the CLI contract.

    Each member maps to one of the six capability buckets named in
    ``API-4`` of ``feature_list.json``. Every member is covered by at
    least one entry in :data:`COMMANDS`; the architecture check
    enforces this superset relation.

    Example:
        >>> Group.KILL_SWITCH
        <Group.KILL_SWITCH: 'kill-switch'>
    """

    KILL_SWITCH = "kill-switch"
    STRATEGY = "strategy"
    LIVE = "live"
    HOT_SWAP = "hot-swap"
    READINESS = "readiness"
    ADMIN = "admin"


class ExitCode(IntEnum):
    """Documented exit codes for every CLI command.

    Concrete handlers map outcomes onto these codes; the contract test
    ensures every command's documented exit codes are a subset.

    Example:
        >>> ExitCode.OK
        <ExitCode.OK: 0>
        >>> ExitCode.NOT_IMPLEMENTED
        <ExitCode.NOT_IMPLEMENTED: 64>
    """

    OK = 0
    USAGE_ERROR = 2
    CONFIRMATION_REQUIRED = 3
    NOT_FOUND = 4
    NOT_READY = 5
    TIMEOUT = 6
    NOT_IMPLEMENTED = 64


@dataclass(frozen=True, slots=True)
class Argument:
    """Declarative description of one CLI argument or option.

    Attributes:
        name: Argument identifier; flag arguments are written with
            their ``--`` prefix, positionals without.
        summary: One-line human-readable description.
        required: Whether the argument is required.
        is_flag: Whether the argument is a boolean flag (no value).
        default: Optional default value rendered into the manual.

    Example:
        >>> Argument(
        ...     name="--confirm",
        ...     summary="Two-step confirmation token (SRS-SAFE-001 / UI-4).",
        ...     required=True,
        ...     is_flag=True,
        ... ).is_flag
        True
    """

    name: str
    summary: str
    required: bool = False
    is_flag: bool = False
    default: str | None = None


@dataclass(frozen=True, slots=True)
class Command:
    """Declarative description of one CLI command.

    The contract is offline: argument and exit-code metadata is
    reference material for the manual snapshot; concrete behaviours
    arrive with the downstream feature that owns the workflow.

    Attributes:
        group: Top-level group this command belongs to (see
            :class:`Group`).
        name: Sub-command name as typed after the group; ``""`` for the
            group's default action.
        summary: One-line human-readable description.
        srs_refs: Tuple of SRS / SyRS / NFR identifiers this command
            traces to; must contain at least one entry.
        arguments: Tuple of :class:`Argument` declarations in the order
            they should appear in ``--help`` output.
        exit_codes: Tuple of :class:`ExitCode` values this command may
            return. Must include :attr:`ExitCode.OK`.
        requires_confirmation: Mark commands that perform irreversible
            actions; the runner enforces a ``--confirm`` argument.

    Example:
        >>> Command(
        ...     group=Group.KILL_SWITCH,
        ...     name="activate",
        ...     summary="Activate the QuantConnect Liquidate sequence.",
        ...     srs_refs=("SRS-SAFE-001",),
        ...     arguments=(
        ...         Argument(
        ...             name="--confirm",
        ...             summary="Confirmation token.",
        ...             required=True,
        ...             is_flag=True,
        ...         ),
        ...     ),
        ...     exit_codes=(ExitCode.OK, ExitCode.CONFIRMATION_REQUIRED),
        ...     requires_confirmation=True,
        ... ).requires_confirmation
        True
    """

    group: Group
    name: str
    summary: str
    srs_refs: tuple[str, ...]
    arguments: tuple[Argument, ...] = field(default_factory=tuple)
    exit_codes: tuple[ExitCode, ...] = (ExitCode.OK,)
    requires_confirmation: bool = False

    @property
    def invocation(self) -> str:
        """Canonical ``group sub-command`` invocation string.

        Example:
            >>> Command(
            ...     group=Group.READINESS,
            ...     name="check",
            ...     summary="Show readiness state.",
            ...     srs_refs=("SYS-76",),
            ... ).invocation
            'readiness check'
        """

        if not self.name:
            return self.group.value
        return f"{self.group.value} {self.name}"


