def commands_by_group(group: Group) -> tuple[Command, ...]:
    """Return every command declared for the given group bucket.

    Example:
        >>> any(c.name == "activate"
        ...     for c in commands_by_group(Group.KILL_SWITCH))
        True
    """

    return tuple(command for command in COMMANDS if command.group is group)


def find_command(group: Group, name: str) -> Command | None:
    """Return the unique command matching ``group``/``name`` or ``None``.

    Example:
        >>> find_command(Group.READINESS, "check").summary  # doctest: +ELLIPSIS
        'Print readiness state...'
    """

    for command in COMMANDS:
        if command.group is group and command.name == name:
            return command
    return None
