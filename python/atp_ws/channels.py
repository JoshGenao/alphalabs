"""Declarative WebSocket API contract surface for ATP dashboard updates.

This module is the single source of truth for the ATP WebSocket API
described by ``API-3`` in ``feature_list.json`` and traced to
``SRS-UI-001`` through ``SRS-UI-004`` in ``docs/SRS.md`` §7. It declares
event channels, client commands, message types, and the loopback-only
auth model. It does **not** start a WebSocket server: real publishers
land with downstream features (``EXE-1``, ``ORCH-1``, ``MD-1``,
``RESV-1``, ``LOG-1``, ``NOTIF-1``).

The contract surface is introspectable at runtime via
:data:`EVENT_CHANNELS` and :data:`CLIENT_COMMANDS`, and is rendered to
an AsyncAPI 2.6 document by :mod:`atp_ws.asyncapi`. The frozen snapshot
``python/atp_ws/asyncapi.json`` is regenerated by
``python tools/websocket_api_check.py --update``.

SRS trace
---------
``SRS-UI-001`` (live performance dashboard), ``SRS-UI-002`` (active
strategy inventory), ``SRS-UI-003`` (account / Reservoir overview),
``SRS-UI-004`` (backtest history view, served via REST), with
event-level traces to ``SYS-36``..``SYS-39``, ``SYS-39a``, ``SYS-41``,
``SYS-43b``, ``SYS-46``, ``SYS-48``, ``SYS-58``, ``SYS-61``,
``SYS-79``, ``SRS-LOG-001``, ``SRS-NOTIF-001``, ``SRS-RESV-002``,
``NFR-P2`` (5 s refresh ceiling), and ``SRS-SEC-002``
(loopback / single-user policy).
"""

from __future__ import annotations

from dataclasses import dataclass
from enum import StrEnum

# --------------------------------------------------------------------------- #
# Auth and bind policy  [SRS-SEC-002]
# --------------------------------------------------------------------------- #


BIND_HOST: str = "127.0.0.1"
"""Default WebSocket bind address.

Per ``SRS-SEC-002`` and the API-3 ``Local single-user deployment
controls`` constraint, the operator WebSocket API binds to loopback (or
RFC 1918) only and does not negotiate authentication tokens or RBAC.
"""

AUTH_MODEL: str = "local-single-user"
"""Auth model identifier.

The platform is single-operator (``StRS`` constraint C-6); the
WebSocket API trusts the local caller. No bearer tokens, sessions, or
RBAC are defined.
"""

WS_PATH: str = "/ws/v1"
"""Versioned WebSocket endpoint path.

Operators connect to ``ws://127.0.0.1:8080{WS_PATH}`` and subscribe to
event channels by name. Channel-level URL routing (e.g. one socket per
channel) is intentionally avoided; the SUBSCRIBE protocol multiplexes
all channels over a single connection.
"""

MAX_REFRESH_SECONDS: int = 5
"""Refresh-cadence ceiling (``NFR-P2``: ≤5 s dashboard refresh).

Channels with periodic publishing must declare a ``refresh_seconds``
between ``0`` (event-driven, no fixed cadence) and this ceiling.
"""


# --------------------------------------------------------------------------- #
# Direction, message types, and channel buckets
# --------------------------------------------------------------------------- #


class Direction(StrEnum):
    """Direction of a WebSocket message relative to the dashboard server.

    Example:
        >>> Direction.SERVER_TO_CLIENT
        <Direction.SERVER_TO_CLIENT: 'SERVER_TO_CLIENT'>
    """

    SERVER_TO_CLIENT = "SERVER_TO_CLIENT"
    CLIENT_TO_SERVER = "CLIENT_TO_SERVER"


class MessageType(StrEnum):
    """Discriminator for the seven framed message types on the socket.

    Event payloads carry ``type=EVENT``; control plane messages carry
    one of the SUBSCRIBE / UNSUBSCRIBE / ACK / ERROR / HEARTBEAT_PING /
    HEARTBEAT_PONG variants.

    Example:
        >>> MessageType.SUBSCRIBE
        <MessageType.SUBSCRIBE: 'SUBSCRIBE'>
    """

    EVENT = "EVENT"
    SUBSCRIBE = "SUBSCRIBE"
    UNSUBSCRIBE = "UNSUBSCRIBE"
    ACK = "ACK"
    ERROR = "ERROR"
    HEARTBEAT_PING = "HEARTBEAT_PING"
    HEARTBEAT_PONG = "HEARTBEAT_PONG"


class Channel(StrEnum):
    """Event channel buckets covered by the WebSocket API contract.

    Each member maps to one of the eight buckets named in ``API-3`` of
    ``feature_list.json``. Every member is covered by at least one entry
    in :data:`EVENT_CHANNELS`; the architecture check enforces this
    superset relation.

    Example:
        >>> Channel.PNL
        <Channel.PNL: 'PNL'>
    """

    PNL = "PNL"
    METRICS = "METRICS"
    ACCOUNT_STATUS = "ACCOUNT_STATUS"
    HEARTBEAT = "HEARTBEAT"
    LOGS = "LOGS"
    ALERTS = "ALERTS"
    RESERVOIR_RANKING = "RESERVOIR_RANKING"
    STRATEGY_STATE = "STRATEGY_STATE"


@dataclass(frozen=True, slots=True)
class EventChannel:
    """Declarative description of one server-to-client event channel.

    The contract is offline: ``payload_fields`` is a placeholder
    field-name tuple for the AsyncAPI snapshot; concrete payload schemas
    arrive with the downstream feature that owns the publisher.

    Attributes:
        name: Channel bucket identifier (see :class:`Channel`).
        summary: One-line human-readable description.
        srs_refs: Tuple of SRS / SyRS / NFR identifiers this channel
            traces to; must contain at least one entry.
        payload_fields: Field names emitted with each event payload.
        refresh_seconds: Maximum interval between publishes for periodic
            channels. ``0`` means event-driven (no fixed cadence).
            ``NFR-P2`` caps this at :data:`MAX_REFRESH_SECONDS`.
        requires_subscription: Whether the client must SUBSCRIBE before
            receiving events on this channel. Defaults to ``True``.

    Example:
        >>> EventChannel(
        ...     name=Channel.HEARTBEAT,
        ...     summary="Market data and broker heartbeat staleness.",
        ...     srs_refs=("SRS-UI-001", "SYS-39"),
        ...     payload_fields=("feed", "staleness_seconds"),
        ...     refresh_seconds=1,
        ... ).requires_subscription
        True
    """

    name: Channel
    summary: str
    srs_refs: tuple[str, ...]
    payload_fields: tuple[str, ...]
    refresh_seconds: int = 5
    requires_subscription: bool = True


@dataclass(frozen=True, slots=True)
class ClientCommand:
    """Declarative description of one client-to-server control message.

    Attributes:
        type: Discriminator value (must be one of the CLIENT_TO_SERVER
            members of :class:`MessageType`).
        summary: One-line human-readable description.
        srs_refs: Tuple of SRS / SyRS / NFR identifiers; must be non-empty.
        request_fields: Field names accepted in the command payload.
        response_message: The :class:`MessageType` the server emits in
            response (typically ``ACK`` or ``HEARTBEAT_PONG``).

    Example:
        >>> ClientCommand(
        ...     type=MessageType.SUBSCRIBE,
        ...     summary="Subscribe to one or more channels.",
        ...     srs_refs=("SRS-UI-001",),
        ...     request_fields=("channels",),
        ...     response_message=MessageType.ACK,
        ... ).type
        <MessageType.SUBSCRIBE: 'SUBSCRIBE'>
    """

    type: MessageType
    summary: str
    srs_refs: tuple[str, ...]
    request_fields: tuple[str, ...]
    response_message: MessageType


