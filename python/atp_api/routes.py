def routes_by_capability(capability: Capability) -> Tuple[Route, ...]:
    """Return every route declared for the given capability bucket.

    Example:
        >>> any(r.path == "/api/v1/kill-switch"
        ...     for r in routes_by_capability(Capability.KILL_SWITCH))
        True
    """

    return tuple(route for route in ROUTES if route.capability is capability)
