"""Declarative REST API contract surface for ATP operator workflows.

This module is the single source of truth for the ATP REST API described by
``API-2`` in ``feature_list.json`` and traced to ``SRS-API-001`` in
``docs/SRS.md`` §7. It declares routes, capabilities, and the loopback-only
auth model. It does **not** start an HTTP server: real request handlers will
land with downstream features (``EXE-1``, ``ORCH-1``, ``RESV-1``, ...).

The contract surface is introspectable at runtime via :data:`ROUTES`, and is
turned into an OpenAPI 3.1 document by :mod:`atp_api.openapi`. The frozen
snapshot ``python/atp_api/openapi.json`` is regenerated by
``python tools/rest_api_check.py --update``.

SRS trace
---------
``SRS-API-001`` (REST/CLI/dashboard operator workflows), with route-level
traces to ``SRS-ORCH-004``/``SRS-ORCH-005`` (lifecycle, version, rollback),
``SRS-SAFE-001`` (kill switch), ``SRS-RESV-002``..``SRS-RESV-006`` (Reservoir
ranking, Hot-Swap), ``SRS-BT-001``/``SRS-BT-009`` (backtests),
``SRS-DATA-002`` (watchlist), ``SRS-LOG-001`` (logs), ``SRS-NOTIF-001``
(alerts), ``SRS-SEC-002`` (loopback / RFC 1918 binding).
"""

from __future__ import annotations

from dataclasses import dataclass, field
from enum import StrEnum
from typing import Tuple


# --------------------------------------------------------------------------- #
# Auth and bind policy  [SRS-SEC-002]
# --------------------------------------------------------------------------- #


BIND_HOST: str = "127.0.0.1"
"""Default REST API bind address.

Per ``SRS-SEC-002`` and the API-2 ``Local single-user deployment controls``
constraint, the operator REST API binds to loopback (or RFC 1918) only and
does not expose authentication tokens or RBAC.
"""

AUTH_MODEL: str = "local-single-user"
"""Auth model identifier.

The platform is single-operator (``StRS`` constraint C-6); the REST API
trusts the local caller. No bearer tokens, sessions, or RBAC are defined.
"""


# --------------------------------------------------------------------------- #
# Method, capability, and route shape
# --------------------------------------------------------------------------- #


class Method(StrEnum):
    """HTTP method for a REST route.

    Example:
        >>> Method.POST
        <Method.POST: 'POST'>
    """

    GET = "GET"
    POST = "POST"
    PUT = "PUT"
    DELETE = "DELETE"


class Capability(StrEnum):
    """Operator capability buckets covered by the REST API contract.

    Each member maps to one of the eleven buckets named in ``API-2`` of
    ``feature_list.json``. Every member is covered by at least one entry in
    :data:`ROUTES`; the architecture check enforces this superset relation.

    Example:
        >>> Capability.KILL_SWITCH
        <Capability.KILL_SWITCH: 'KILL_SWITCH'>
    """

    STRATEGY_LIFECYCLE = "STRATEGY_LIFECYCLE"
    LIVE_DESIGNATION = "LIVE_DESIGNATION"
    KILL_SWITCH = "KILL_SWITCH"
    HOT_SWAP = "HOT_SWAP"
    BACKTEST_LAUNCH = "BACKTEST_LAUNCH"
    BACKTEST_QUERY = "BACKTEST_QUERY"
    RESERVOIR_RANKING = "RESERVOIR_RANKING"
    WATCHLIST_CONFIG = "WATCHLIST_CONFIG"
    SYSTEM_STATUS = "SYSTEM_STATUS"
    LOGS = "LOGS"
    ALERTS = "ALERTS"


@dataclass(frozen=True, slots=True)
class Route:
    """Declarative description of one REST endpoint.

    The contract is offline: ``request_fields`` and ``response_fields`` are
    placeholder field-name tuples for the OpenAPI snapshot; concrete schemas
    arrive with the downstream feature that implements the handler.

    Attributes:
        method: HTTP method (see :class:`Method`).
        path: URL path beginning with ``/api/v1/``. Path parameters are
            written as ``{name}`` segments.
        capability: Operator capability bucket this route belongs to.
        summary: One-line human-readable description of the endpoint.
        srs_refs: Tuple of SRS / SyRS / NFR identifiers this route traces to;
            must contain at least one entry.
        request_fields: Field names accepted in the request (query for GET,
            body for POST/PUT). Empty tuple is allowed for parameter-less
            endpoints.
        response_fields: Field names returned in the success response body.
        requires_confirmation: Mark routes that perform irreversible actions
            and need a UI two-step confirmation token (modeled here as a
            ``confirm`` query parameter, see ``UI-4``).

    Example:
        >>> Route(
        ...     method=Method.GET,
        ...     path="/api/v1/system/status",
        ...     capability=Capability.SYSTEM_STATUS,
        ...     summary="Return service health and readiness state.",
        ...     srs_refs=("SYS-76",),
        ...     request_fields=(),
        ...     response_fields=("ib_gateway", "ssd", "nas", "services"),
        ... ).method
        <Method.GET: 'GET'>
    """

    method: Method
    path: str
    capability: Capability
    summary: str
    srs_refs: Tuple[str, ...]
    request_fields: Tuple[str, ...] = field(default_factory=tuple)
    response_fields: Tuple[str, ...] = field(default_factory=tuple)
    requires_confirmation: bool = False


